# Tier-1 validation
# ---------------------------------------------------------------------------

PLACEHOLDER_RE = re.compile(r"\{\{[A-Z_][A-Z0-9_]*\}\}")
FRONTMATTER_KEY_RE = re.compile(r"^([A-Za-z][A-Za-z0-9_-]*)\s*:\s*(.*)$")


def parse_frontmatter(text: str):
    """Return (frontmatter_dict, body) or (None, text) if no frontmatter.

    Minimal YAML parser — only top-level `key: value` pairs needed for
    Tier-1 validation. Multi-line values, lists and nested objects are not
    supported (and not used in our SKILL.md / agent files)."""
    if not text.startswith("---\n"):
        return None, text
    end = text.find("\n---\n", 4)
    if end == -1:
        return None, text
    block = text[4:end]
    body = text[end + 5:]
    fm = {}
    for line in block.splitlines():
        if not line.strip() or line.lstrip().startswith("#"):
            continue
        m = FRONTMATTER_KEY_RE.match(line)
        if m:
            fm[m.group(1)] = m.group(2).strip()
    return fm, body


def collect_artifacts(plugin_root: str):
    """Yield (kind, path) for every file we want to validate."""
    agents_dir = os.path.join(plugin_root, "agents")
    if os.path.isdir(agents_dir):
        for fname in sorted(os.listdir(agents_dir)):
            if fname.endswith(".md"):
                yield "agent", os.path.join(agents_dir, fname)

    skills_dir = os.path.join(plugin_root, "skills")
    if os.path.isdir(skills_dir):
        for skill_name in sorted(os.listdir(skills_dir)):
            skill_md = os.path.join(skills_dir, skill_name, "SKILL.md")
            if os.path.isfile(skill_md):
                yield "skill", skill_md


def validate(plugin_root: str) -> list:
    """Run Tier-1 validation. Returns list of error strings (empty = OK)."""
    errors = []
    seen_names = {"agent": {}, "skill": {}}

    for kind, path in collect_artifacts(plugin_root):
        rel = os.path.relpath(path, plugin_root)
        try:
            with open(path, encoding="utf-8") as f:
                text = f.read()
        except OSError as e:
            errors.append(f"{rel}: cannot read ({e})")
            continue

        fm, body = parse_frontmatter(text)
        if fm is None:
            errors.append(f"{rel}: missing or malformed frontmatter")
            continue

        for required in ("name", "description"):
            if not fm.get(required):
                errors.append(f"{rel}: frontmatter missing `{required}`")

        unresolved = sorted(set(PLACEHOLDER_RE.findall(text)))
        if unresolved:
            errors.append(f"{rel}: unresolved placeholder(s) {', '.join(unresolved)}")

        name = fm.get("name")
        if name:
            prev = seen_names[kind].get(name)
            if prev:
                errors.append(f"{rel}: duplicate {kind} name `{name}` (also in {prev})")
            else:
                seen_names[kind][name] = rel

    return errors


# ---------------------------------------------------------------------------
# --check mode (CI: build into tempdir, validate, compare with working tree)
# ---------------------------------------------------------------------------


def collect_relpaths(root: str) -> set:
    """Return set of file paths under root, relative to root."""
    out = set()
    if not os.path.isdir(root):
        return out
    for dirpath, _, filenames in os.walk(root):
        for fname in filenames:
            full = os.path.join(dirpath, fname)
            out.add(os.path.relpath(full, root))
    return out


def diff_dirs(actual: str, expected: str) -> list:
    """Return list of human-readable diff messages between two directory trees."""
    diffs = []
    actual_files = collect_relpaths(actual)
    expected_files = collect_relpaths(expected)

    for missing in sorted(expected_files - actual_files):
        diffs.append(f"  missing in working tree: {missing}")
    for extra in sorted(actual_files - expected_files):
        diffs.append(f"  unexpected in working tree: {extra}")
    for common in sorted(actual_files & expected_files):
        a = os.path.join(actual, common)
        b = os.path.join(expected, common)
        if not filecmp.cmp(a, b, shallow=False):
            diffs.append(f"  content differs: {common}")
    return diffs


def run_check() -> int:
    """Build into a tempdir, validate, compare with working tree. Returns exit code."""
    print("Running build --check (no working tree modifications)\n")

    with tempfile.TemporaryDirectory(prefix="caronowf-build-") as tmp:
        print(f"[plugin] building into {tmp}")
        try:
            build_plugin(tmp)
        except Exception as e:
            print(f"\nBUILD ERROR: {e}", file=sys.stderr)
            return 2

        print("\n[validate]")
        errors = validate(tmp)
        if errors:
            for e in errors:
                print(f"  {e}", file=sys.stderr)
            print(f"\n{len(errors)} validation error(s).", file=sys.stderr)
            return 1
        print("  OK")

        print("\n[diff vs working tree]")
        all_diffs = []
        for sub in ("agents", "skills"):
            d = diff_dirs(os.path.join(ROOT, sub), os.path.join(tmp, sub))
            if d:
                all_diffs.append(f"\n  {sub}/")
                all_diffs.extend(d)

        if all_diffs:
            print("Working tree is out of sync with src/. Run `python3 src/scripts/build.py` and commit.", file=sys.stderr)
            for line in all_diffs:
                print(line, file=sys.stderr)
            return 1

        print("  in sync")

    print("\nAll checks passed.")
    return 0


def main() -> None:
    parser = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    parser.add_argument("target", nargs="?", choices=ALL_TARGETS, help="Build only this target (default: all)")
    parser.add_argument("--check", action="store_true", help="CI mode: build into tempdir, validate, compare to working tree, exit 1 on drift")
    args = parser.parse_args()

    if args.check:
        sys.exit(run_check())

    targets = [args.target] if args.target else ALL_TARGETS

    print("Building from src/")

    total = 0
    for t in targets:
        print(f"\n[{t}]")
        if t == "plugin":
            total += build_plugin(ROOT)
        # elif t == "opencode":
        #     total += build_opencode(ROOT)

    print(f"\nDone. {total} file(s) processed.")

    print("\n[validate]")
    errors = validate(ROOT)
    if errors:
        for e in errors:
            print(f"  {e}", file=sys.stderr)
        print(f"\n{len(errors)} validation error(s).", file=sys.stderr)
        sys.exit(1)
    print("  OK")


if __name__ == "__main__":
    main()
