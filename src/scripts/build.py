#!/usr/bin/env python3
"""Build plugin artifacts from src/.

Outputs:

  Repo root (Claude Code plugin — plugin `carono-wf` via marketplace `carono`):
    agents/*.md                         (from src/agents/)
    skills/<name>/...                   (from src/skills/)

The plugin manifest (.claude-plugin/plugin.json and .claude-plugin/marketplace.json)
is static — committed as-is, not generated by this script.

Usage:
    python build.py              # build all targets, write to repo root
    python build.py plugin       # only the plugin (repo root)
    python build.py --check      # CI-friendly: build into tempdir, validate,
                                   compare with current artifacts; exit 1 on
                                   any drift or validation error. Does NOT
                                   modify the working tree.

After every build (and inside --check) Tier-1 validation runs over the produced
artifacts: frontmatter has `name`/`description`, no unresolved `{{VAR}}`,
agent/skill names are unique within their group.

NOTE: opencode build is temporarily disabled — see commented-out build_opencode()
below. Re-enable when we're ready to validate compatibility again.
"""

import argparse
import filecmp
import os
import re
import shutil
import subprocess
import sys
import tempfile

SCRIPT_DIR = os.path.dirname(os.path.abspath(__file__))
ROOT = os.path.dirname(os.path.dirname(SCRIPT_DIR))
SRC = os.path.join(ROOT, "src")
DIST = os.path.join(ROOT, "dist")
FORMAT_SCRIPT = os.path.join(SCRIPT_DIR, "format.py")

ALL_TARGETS = ["plugin"]


def load_template_variables() -> dict:
    """Read rules and templates files and return as template variables."""
    variables = {}

    sec_path = os.path.join(SRC, "rules", "security.md")
    if os.path.isfile(sec_path):
        with open(sec_path, encoding="utf-8") as f:
            variables["{{RULES_SECURITY}}"] = f.read()

    stop_path = os.path.join(SRC, "rules", "stop-and-ask.md")
    if os.path.isfile(stop_path):
        with open(stop_path, encoding="utf-8") as f:
            variables["{{RULES_STOP_AND_ASK}}"] = f.read()

    for tpl_name in ("WORKFLOW", "PROJECT", "TECH", "BOT", "MISSION", "ROADMAP", "MANUAL_INTERVENTIONS"):
        tpl_path = os.path.join(SRC, "templates", f"{tpl_name}.md")
        if os.path.isfile(tpl_path):
            with open(tpl_path, encoding="utf-8") as f:
                variables[f"{{{{TEMPLATE_{tpl_name}}}}}"] = f.read()

    return variables


def substitute_variables(content: str, variables: dict) -> str:
    for placeholder, value in variables.items():
        content = content.replace(placeholder, value)
    return content


def run_format(input_path: str, platform: str, output_path: str, variables: dict, out_root: str) -> bool:
    """Convert a Markdown file via format.py, with template variable substitution."""
    with open(input_path, encoding="utf-8") as f:
        content = f.read()
    content = substitute_variables(content, variables)

    tmp_path = input_path + ".tmp.resolved"
    with open(tmp_path, "w", encoding="utf-8") as f:
        f.write(content)

    cmd = [
        sys.executable, FORMAT_SCRIPT,
        tmp_path, platform,
        "--style", "markdown",
        "--output", output_path,
    ]
    result = subprocess.run(cmd, capture_output=True, text=True)
    os.remove(tmp_path)

    if result.returncode != 0:
        print(f"  ERROR: {result.stderr.strip()}", file=sys.stderr)
        return False

    rel_in = os.path.relpath(input_path, ROOT)
    rel_out = os.path.relpath(output_path, out_root)
    print(f"  {rel_in} -> {rel_out}")
    return True


def copy_skills(dst_root: str, variables: dict) -> int:
    """Copy src/skills/<name>/ to <dst_root>/<name>/ with SKILL.md variable substitution."""
    skills_src = os.path.join(SRC, "skills")
    if not os.path.isdir(skills_src):
        return 0

    os.makedirs(dst_root, exist_ok=True)
    count = 0

    for skill_name in sorted(os.listdir(skills_src)):
        src_dir = os.path.join(skills_src, skill_name)
        if not os.path.isdir(src_dir):
            continue

        dst_dir = os.path.join(dst_root, skill_name)
        os.makedirs(dst_dir, exist_ok=True)

        for dirpath, dirnames, filenames in os.walk(src_dir):
            dirnames.sort()
            rel = os.path.relpath(dirpath, src_dir)
            out_sub = dst_dir if rel == "." else os.path.join(dst_dir, rel)
            os.makedirs(out_sub, exist_ok=True)

            for fname in sorted(filenames):
                src_file = os.path.join(dirpath, fname)
                dst_file = os.path.join(out_sub, fname)

                if fname == "SKILL.md":
                    with open(src_file, encoding="utf-8") as f:
                        content = f.read()
                    content = substitute_variables(content, variables)
                    with open(dst_file, "w", encoding="utf-8") as f:
                        f.write(content)
                else:
                    shutil.copy2(src_file, dst_file)

                rel_in = os.path.relpath(src_file, ROOT)
                rel_out = os.path.relpath(dst_file, dst_root)
                print(f"  {rel_in} -> skills/{rel_out}")
                count += 1

    return count


def build_plugin(out_root: str) -> int:
    """Build plugin layout into out_root: <out_root>/agents/ and <out_root>/skills/."""
    variables = load_template_variables()
    total = 0

    agents_dst = os.path.join(out_root, "agents")
    skills_dst = os.path.join(out_root, "skills")
    for path in (agents_dst, skills_dst):
        if os.path.isdir(path):
            shutil.rmtree(path)

    os.makedirs(agents_dst, exist_ok=True)
    src_dir = os.path.join(SRC, "agents")
    if os.path.isdir(src_dir):
        for fname in sorted(os.listdir(src_dir)):
            if not fname.endswith(".md"):
                continue
            out = os.path.join(agents_dst, fname)
            if run_format(os.path.join(src_dir, fname), "claude-code", out, variables, out_root):
                total += 1

    total += copy_skills(skills_dst, variables)
    return total


